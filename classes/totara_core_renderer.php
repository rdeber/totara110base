defined('MOODLE_INTERNAL') || die();

global $CFG;

require_once("{$CFG->dirroot}/totara/core/renderer.php");

class theme_totara110base_totara_core_renderer extends totara_core_renderer {
    /**
     * Renders tabtree
     *
     * @param tabtree $tabtree
     * @return string
     */
    protected function render_tabtree(tabtree $tabtree) {
        if (empty($tabtree->subtree)) {
            return '';
        }
        $firstrow = $secondrow = '';
        $classes = 'tabtree';
        foreach ($tabtree->subtree as $tab) {
            $firstrow .= $this->render($tab);
            if (($tab->selected || $tab->activated) && !empty($tab->subtree) && $tab->subtree !== array()) {
                $classes .= ' tabtree2';
                $secondrow = $this->tabtree($tab->subtree);
            }
        }
        return html_writer::tag('ul', $firstrow, array('class' => 'nav nav-tabs '.$classes)).$secondrow;
    }

}
